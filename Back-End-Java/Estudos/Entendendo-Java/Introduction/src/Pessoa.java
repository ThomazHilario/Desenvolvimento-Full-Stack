public class Pessoa{
    // Atributos
    private String name;
    private int age;
    private String data_nascimento;
    private String cpf;

    // Constructor
    public Pessoa(String name, int age, String data_nascimento, String cpf){
        this.name = name;
        this.age = age;
        this.data_nascimento = data_nascimento;
        this.cpf = cpf;
    }
}